/*
Од тастатура се внесуваат димензиите на една матрица (m,n <= 100), а потоа и елементите од матрицата. Да се трансформира матрицата така што средниот елемент во секоја редица ќе се замени со разликата (по апсолутна вредност) на сумата на елементите во првата половина од редицата и сумата на елементите во втората половина на редицата. Ако матрицата има парен број колони, се менува вредноста на средните два елемента. Средниот/те елемент/и влегува/ат во сумите (при непарен број на колони, средниот елемент влегува во двете суми!). Да се испечати на екран променетата матрица.

Пример.

Влез:

m = 4

n = 4

1 3 -5 4

2 10 2 10

7 2 3 5

3 2 10 3

Излез:

1 5 5 4

2 0 0 10

7 1 1 5

3 8 8 3

For example:

Input	        Result
4 5
1 0 0 3 1
0 0 1 2 5
1 2 1 0 0
1 1 0 0 3
                1 0 3 3 1
                0 0 7 2 5
                1 2 3 0 0
                1 1 1 0 3
 */
#include <stdio.h>
#include <math.h>

int main(){
    int m,n;
    scanf("%d%d",&m,&n);

    int a[100][100];
    for(int i=0;i<m;i++){
        for(int j=0;j<n;j++){
            scanf("%d",&a[i][j]);
        }
    }
    for(int i=0;i<m;i++){
        int suma1=0,suma2=0;

        if(n%2==1){
            for(int j=0;j<=n/2;j++){
                suma1+=a[i][j];
            }
            for(int j=n/2;j<n;j++){
                suma2+=a[i][j];
            }
            a[i][n/2]=fabs(suma1-suma2);
        }

        else {
            for(int j=0;j<=(n/2)-1;j++){
                suma1+=a[i][j];
            }
            for(int j=n/2;j<n;j++){
                suma2+=a[i][j];
            }
            a[i][n/2]=fabs(suma1-suma2);
            a[i][(n/2)-1]=fabs(suma1-suma2);
        }
    }

    for(int i=0;i<m;i++){
        for(int j=0;j<n;j++){
            printf("%d ",a[i][j]);
        }
        printf("\n");
    }

    return 0;
}